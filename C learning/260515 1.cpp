#include <stdio.h>

/* 全局变量
 * 1.定义在函数外面的变量是全局变量
 * 2.全局变量具有全局的生存期和作用域
 * 3.任何函数内部都可以使用它们
 * 4.如果没有显式初始化，编译器会自动初始化（基本类型为0，指针为NULL）
 */
int f(void);
int g(void);
void h(void);
int a = 12;      // 已初始化的全局变量
int b;           // 未初始化的全局变量，默认初始化为0
int *p;          // 未初始化的全局指针，默认初始化为NULL

/* 注意：全局变量初始化只能用编译时刻已知的值
 * 错误示例：
 * int All = f();  // 错误：不能用函数返回值初始化
 * 正确示例（当gAll是const时）：
 * const int gAll = 10;
 * int All = gAll;  // 正确：可以用const常量初始化
 */

int main()
{
    printf("-------全局变量测试-------\n");
    printf("在main函数中: a = %d\n", a);
    printf("在main函数中: b = %d\n", b);  // 没有初始化的全局变量会得到0值
    printf("在main函数中: p = %p\n", (void*)p);  // 指针会得到NULL值
    
    f();  // 调用函数f，测试全局变量在函数中的访问
    
    printf("再次在main函数中: a = %d\n", a);
    
    printf("\n-------静态本地变量测试-------\n"); 
    g();  // 第一次调用g
    g();  // 第二次调用g
    g();  // 第三次调用g
    h();  // 调用h函数查看内存地址
    
    return 0;
}

/* 函数f：测试全局变量的访问和局部变量对全局变量的隐藏 */
int f(void)
{
    printf("\n在f函数中: a = %d\n", a);
    a += 2;  // 修改全局变量a
    printf("在f函数中修改后: a = %d\n", a);
    
    int b = 2;  // 定义同名的局部变量b，会隐藏全局变量b
    printf("在f函数中: b = %d\n", b);  // 这里访问的是局部变量b，全局变量b被隐藏
    
    return a;
}

/* 函数g：测试静态本地变量和普通本地变量的区别
 * 静态本地变量特点：
 * 1.在本地变量定义时加上static修饰符就成为静态本地变量
 * 2.当函数离开时，静态本地变量会继续存在并保持其值
 * 3.静态本地变量的初始化只会在第一次进入这个函数时做，以后进入函数会保持上次离开时的值
 */
int g(void)
{
    printf("\n---静态本地变量测试---\n");
    static int c = 2;  // 静态本地变量，只初始化一次
    printf("在g函数中: 静态变量c = %d\n", c);
    c += 2;  // 每次调用都会在原有值上增加2
    printf("在g函数中修改后: 静态变量c = %d\n", c);
    
    printf("\n---普通本地变量测试---\n");
    int d = 2;  // 普通本地变量，每次调用都会重新初始化为2
    printf("在g函数中: 普通变量d = %d\n", d);
    d += 2;  // 每次调用都会从2增加到4
    printf("在g函数中修改后: 普通变量d = %d\n", d);
    
    return c;
}

/* 函数h：打印变量地址，展示静态本地变量的本质
 * 静态本地变量实际上是特殊的全局变量：
 * 1.它们位于相同的内存区域（数据段/静态存储区）
 * 2.静态本地变量具有全局的生存期，函数内的局部作用域
 * 3.static在这里的意思是局部作用域（只能在本函数内访问）
 */
void h(void)
{
    printf("\n---变量地址对比（理解存储区域）---\n");
    static int e = 10;  // 静态本地变量
    int f = 5;          // 普通本地变量（自动变量）
    
    printf("全局变量地址:\n");
    printf("&a = %p  (位于数据段/静态存储区)\n", (void*)&a);
    
    printf("\n静态本地变量地址:\n");
    printf("&e = %p  (位于数据段/静态存储区，与全局变量相邻)\n", (void*)&e);
    
    printf("\n普通本地变量地址:\n");
    printf("&f = %p  (位于栈区，与其他变量不在同一区域)\n", (void*)&f);
    
    printf("\n观察：全局变量a和静态本地变量e的地址相近，");
    printf("而普通本地变量f的地址与它们相距较远，\n");
    printf("这验证了静态本地变量实际上存储在与全局变量相同的区域。\n");
}
