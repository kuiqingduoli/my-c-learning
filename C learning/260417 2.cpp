#include <stdio.h>

/*
  本代码演示C语言字符串输入的常见陷阱
  重点理解：
  1. 数组大小与字符串长度
      - 数组大小是8，实际只能存7个字符+1个'\0'
      - 超过大小会导致缓冲区溢出（未定义行为）
  
  2. scanf("%s", str)的问题
      - 遇到空白字符（空格、Tab、换行）停止读取
      - 没有长度限制，容易导致溢出
      - 不适合读取包含空格的句子
  
  3. 输入缓冲区的行为
      - 未读取的字符会留在缓冲区
      - 影响后续的输入操作
  
  4. 安全实践
      - 使用宽度限制：scanf("%7s", str)
*/

int main()
{
    /*
      第一部分：演示基本的字符串输入和常见问题
    */
    char word[8];
    // 注意：数组大小为8，最多存储7个字符+1个'\0'
    
    scanf("%s", word);  // 不安全的输入，没有长度限制
    printf("%s\n", word);
    // 示例输入和观察点：
    // 1. 输入"12345678"：刚好8个字符，但没有空间放'\0'，会越界写入'\0'（缓冲区溢出）
    // 2. 输入"helloworld"：10个字符，明显越界，危险！
    // 3. 输入"hello world"：只读取"hello"，" world"留在输入缓冲区
    // 4. 输入"hello"回车"world"：读取"hello"，下次读取会得到"world"
    
    /*
      第二部分：演示连续读取和缓冲区残留
    */
    char word1[8];
    char word2[8];
    
    // 两个连续的scanf读取
    scanf("%s", word1);  // 注意：没有长度限制
    scanf("%s", word2);  // 注意：没有长度限制
    printf("\n");
    printf("%s##%s##\n", word1, word2);
    // 示例输入和观察点：
    // 1. 输入"12345678"和"12345678"：两个都会越界
    // 2. 输入"hello world"：word1得到"hello"，word2得到"world"（空格作为分隔符）
    // 3. 输入"hello"回车"world"：word1得到"hello"，word2得到"world"（换行也是空白字符）
    
    /*
      第三部分：演示安全的输入方式（使用宽度限制）
    */
    char word3[8];
    char word4[8]; 
    
    // 使用%7s限制最多读取7个字符，为'\0'留出空间
    scanf("%7s", word3);  // 安全：不会越界
    scanf("%7s", word4);  // 安全：不会越界
    printf("\n");
    printf("%s##%s##\n", word3, word4);
    // 示例输入和观察点：
    // 1. 输入"123"和"12345678"：
    //    word3得到"123"
    //    word4得到"1234567"（只取前7个字符，'8'留在缓冲区）
    // 2. 输入"12345678"：
    //    word3得到"1234567"（只取前7个字符，'8'留在缓冲区）
    //    word4会读取缓冲区中剩下的"8"（如果之前有字符残留）
    
    return 0;
}
