#include <stdio.h>

int main()
{
    /* 示例1  char[][]: 二维字符数组 (第二维必须指定大小) */
    // char a[][] = {"hello", "world"};  // 错误：第二维大小未指定
    char a1[][10] = {"hello", "world"};  // 正确：第二维大小足够容纳字符串+'\0'
    
    /* 注意：初始化字符串长度不能超过第二维大小-1（保留结尾的'\0'）*/
    // char a1[][10] = {"hello", "12345678910"};  // 错误：第二个字符串长度11 > 10
    
    // 打印二维数组内容
    printf("二维数组a1:\n");
    printf("char a1[0][0]=%c\n",a1[0][0]);
	printf("char a1[0]=%s\n",a1[0]);
    printf("\n");
    
    
    /* 示例2  char *a: 字符指针数组 (每个元素指向字符串字面量) */
    char *a2[] = {"hello", "12345678910"};  // 正确：a2是指针数组
    
    /* 注意：指针数组的元素指向字符串字面量，这些字符串存储在常量区
       不能通过指针修改字符串内容，如a2[0][0]='H'是非法的 */
    
    printf("指针数组a2:\n");
    for (int i = 0; i < 2; i++) {
        printf("a2[%d] = %s (地址:%p)\n", i, a2[i], a2[i]);
    }
    printf("\n");
    
    
    /* 示例3  char **p: 指向指针的指针 (扩展示例) */
   
    char **p = a2;  // p 指向 a2 的第一个元素（a2[0]）
    printf("p[0] = %s\n", p[0]);  // hello
    printf("p[1] = %s\n", p[1]);  // 12345678910


    return 0;
}
