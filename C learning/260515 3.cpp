#include <stdio.h>

/*------编译预处理指令------*/ 
/*1. #开头的是编译预处理指令
 *2. 它们不是C语言的成分，但C语言程序离不开它们
 *3. #define 用来定义一个宏
 */

/*------define------*/ 
/*1.#define <名字> <值> 
 *2.注意没有结尾的分号，因为不是C的语句 
 *3.名字必须是一个单词，值可以是任何东西
 *4. 在C语言的编译器开始编译之前，编译预处理程序(cpp)会把程序中的名字换成值
 *5.完全的文本替换
*/ 

/*------宏------*/
/*1.如果一个宏的值中有其他宏的名字，也是会被替换的
 *2.如果一个宏的值超过一行，最后一行之前的行末需要加'\'
 *3.宏的值后面出现的注释不会被当作宏的值的的一部分
 */ 
 
/*------没有值的宏------*/
/*1. #define _DEBUG 
 *2.这类宏是用于条件编译的
 *后面有其他的编译预处理指令来检查这个宏是否已经被定义过了 
 */
  
/*------预定义的宏------*/
/*1.__LINE__     - 当前行号
 *2.__FILE__     - 当前文件名
 *3.__DATE__     - 编译日期
 *4.__TIME__     - 编译时间
 *5.__STDC__     - 如果编译器符合ANSI C标准，则值为1
 */
 
/*------带参数的宏------*/
/*1. #define cube(x) ((x)*(x)*(x))
 *2.宏可以带参数
 *3.可以带多个参数
 *4.#define MIN(a,b) ((a)>(b)?(b):(a))
 *5.也可以组合(嵌套)使用其他宏 
*/ 

/*------错误定义的宏------*/
/*1.#define A(x) (x*3)     - 缺少括号可能导致运算符优先级问题
 *2.#define B(x) (x)*3     - 整体缺少括号可能导致表达式优先级问题
 */
 
/*------带参数的宏的原则------*/
/*1.一切都要有括号
 *2.整个值要括号
 *3.参数出现的每个地方都要括号 
 *4.#define C(x) ((x)*3)
 */

// 定义圆周率常量
//const double PI=3.14159;  // 使用const定义的常量
#define PI 3.14159         // 使用宏定义的常量

// 定义格式化字符串
#define FORMAT "%f\n"

// 定义PI的两倍，会展开为2*3.14159
#define PI2 2*PI

// 多行宏定义，用于打印PI和PI2的值
#define PRT printf("%f\n",PI); \
            printf("%f\n",PI2)

// 计算立方的宏
#define cube(x) ((x)*(x)*(x))

// 有问题的宏定义示例
#define A(x) (x*3)      // 缺少括号，参数x没有用括号括起来
#define B(x) (x)*3      // 整体缺少括号
#define C(x) ((x)*3)    // 正确的定义方式

int main()
{
    // 计算并打印圆的周长（半径为3.0）
    printf("%f\n", 2*PI*3.0);
    
    // 使用FORMAT宏打印PI2*3.0的值
    printf(FORMAT, PI2*3.0);
    
    // 使用多行宏PRT打印PI和PI2的值
    PRT; 
    
    // 打印当前文件名和行号
    printf("%s:%d\n", __FILE__, __LINE__);
    
    // 打印编译日期和时间
    printf("%s，%s\n", __DATE__, __TIME__);
    
    // 计算并打印5的立方
    printf("cube(5)=%d\n", cube(5));
    
    int i;
    printf("请输入i的值\n");
    scanf("%d", &i);
    
    // 计算并打印输入值i的立方
    printf("cube(i)=%d\n", cube(i));
    
    // 测试有问题的宏A
    printf("A(1+2)=%d\n", A(1+2));  // 展开为(1+2*3)=7，而不是期望的(1+2)*3=9
    
    // 测试有问题的宏B
    printf("18/B(3)=%d\n", 18/B(1));  // 展开为18/(1)*3=54，而不是期望的18/((1)*3)=6
    
    // 测试正确的宏C
    printf("C(1+2)=%d\n", C(1+2));  // 展开为((1+2)*3)=9，正确
    
    // 测试正确的宏C
    printf("18/C(3)=%d\n", 18/C(1));  // 展开为18/((1)*3)=6，正确
    
    return 0; 
}
